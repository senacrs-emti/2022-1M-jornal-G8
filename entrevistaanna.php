    <p class="conteudo"> Quais são os desafios sendo uma líder feminina? 

É muito difícil ser uma mulher em qualquer circunstância. Na parte empresarial, tenho incontáveis experiencias negativas nesse sentido. Entre fornecedores, equipes parceiras e até clientes representando o machismo estrutural são acontecimentos diários. Existe uma diferença de tratamento entre empresários homens e mulheres. Fui desacreditada muitas vezes. Já tive uma equipe de funcionários de cozinha com homens, e era muito complicado o respeito pela liderança feminina. Hoje a Don Loreto é 100% composta de mulheres. Criamos mais oportunidades para a entrada no mundo do trabalho, e assim um ambiente seguro para todas nós trabalharmos. 

Quero que outras mulheres tenham oportunidade de seguir o ramo, assim como eu.  

 
</p>
<p class="conteudo">Que dicas você daria para pessoas que querem seguir no ramo dos negócios, em específico, mulheres? 

As pessoas abrem negócios com o pensamento de ter lucro e enriquecer em um ano, mas na verdade os negócios são a longo prazo. Eles precisam de um bom tempo para amadurecer.  

Estamos agora há 6 anos com a pizzaria, e só agora entendo o suficiente o negócio para conseguir crescer. É natural e muito difícil alguém abrir um negócio e já no início entender profundamente. 

A dica é: estudar o ramo em qual a pessoa vai entrar e conversar com pessoas que estejam abertas a contar os detalhes do ramo. Sempre pensei que se os outros estão tendo bons resultados nas empresas, é um sinal de que o mercado está bom e que eu posso ficar bem também. Quanto mais o meu vizinho for próspero, melhor para mim também. 

 </p>
