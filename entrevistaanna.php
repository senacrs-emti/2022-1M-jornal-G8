    <p class="conteudo">A empresária Michele é a Loreto por trás da Don Loreto: pizzaria delivery localizada no coração da Cidade Baixa, em Porto Alegre (RS). A Don nasceu em 2016 com o objetivo de servir pizzas elaboradas de forma singular e artesanal com produtos de primeira qualidade, homenageando a tradição da família Loreto!  

A equipe de produção, composta 100% por mulheres, oferece diariamente opções de pizzas e massas artesanais. 

A dona e fundadora concedeu uma entrevista onde falou sobre sua história e os desafios do empreendedorismo feminino, incluindo dicas para quem quiser seguir o ramo. 
    <p class="conteudo">De onde surgiu a Don Loreto?  

Eu sempre trabalhei com vendas, e sempre fui apaixonada por trabalhar com metas e desafios. Trabalhei com todo tipo de produto, já fui vendedora de cadeira de rodas, fralda geriátrica, remédios, carros e patrocínios com leis de incentivo, o que acabou sendo o meu maior desafio. 

Abri meu próprio negócio porque o mercado me levou a isso. Na época que trabalhava com captação de recursos o país entrou numa crise onde as empresas iriam ter menos recursos para investir em incentivo fiscal. Para eu não ficar parada ou perder muito a minha renda eu resolvi abrir meu próprio negócio.   

Finalmente abri meu próprio negócio onde eu vendo pizzas e massas e crio minhas próprias receitas. É o produto mais difícil que eu já tive que vender, porque é um produto exclusivamente meu. .</p>
