    <p class="conteudo">“Para nós, é um momento histórico, principalmente aqui no sul do país, fazendo com que todo mundo aqui possa acreditar que a gente não tem limitações, como as que foram impostas por muito tempo, com a dominância sempre do eixo Rio-São Paulo. Estamos mostrando que também temos essa capacidade de realizar grandes eventos e profissionais tão qualificados quanto, além de ótimos artistas. A gente não precisa ir para outro lugar para realizar isso, pode ser aqui onde é nosso quintal, nossa casa” — comemora Keni, idealizador do projeto.  

Grande parte dos artistas se posicionaram politicamente criticando o atual governo, seguidos por momentos descontraídos e animados como pedidos de casamento (show do BIN) e até mesmo lançamento do novo álbum do rapper Djonga.   </p>
