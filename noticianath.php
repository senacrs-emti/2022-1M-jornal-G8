    <p class="conteudo">Festival Lollapalooza tem line-up divulgado 

Evento ocorre nos dias 24, 25 e 26 de março, no Autódromo de Interlagos, em São Paulo. 

 

O festival Lollapalooza Brasil divulgou nesta terça-feira (11) o line-up da edição de 2023. As principais atrações (headliners) da edição são os rappers Drake e Lil Nas X, as bandas Blink-182 e Tame Impala e as cantoras Billie Eilish e Rosalía. 

O Lollapalooza ocorre nos dias 24, 25 e 26 de março, no Autódromo de Interlagos, em São Paulo. Os ingressos estão à venda no site oficial do evento. 

 

Outros destaques são Jane's Addiction, The 1975, Armin Van Buuren, Melanie Martinez, Jamie XX, Aurora, Tove Lo e Kali Uchis. Entre as atrações brasileiras, tocarão Ludmilla, L7nnon, Filipe Ret, Pitty, Pedro Sampaio, Anavitória e Paralamas do Sucesso. </p>
    
