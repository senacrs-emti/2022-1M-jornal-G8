    <p class="conteudo">RAP IN CENA- Nos dias 15 e 16 de outubro, o parque Maurício  Sirotsky Sobrinho, o Harmonia, recebeu um dos maiores festivais de rap do Brasil, contando com três palcos e mais de 60 atrações. O evento criado em 2014 por Keni Martins e Guilherme Camerini é a realização de um sonho para a cena local.  Rap in Cena ainda contou com 300 seguranças, 1.200 vagas de estacionamento, quatro pontos de alimentação, cinco ilhas de banheiros, um ambulatório e três ambulâncias. 

No sábado 15/10, primeiro dia de RiC, inúmeros shows estavam espalhados pelos três palcos: World, Grillz e Hatz. Assim, Froid, Febem, Azzy, Hyperanhas, Kyan, Md Chefe e DomLaike estiveram no Grillz. Enquanto Batalha da Aldeia vs Olimpo, Cynthia Luz, Daguedes, Cristal, Recayd, Djonga, Tasha e Tracie, Chefin, Poze, L7nnon e Filipe Ret levaram uma multidão ao palco principal.  

No domingo (16) houve um reforço no line com Yasmin Limas, Kawe, Long Beatz e Cartel MC’s, Poesia Acústica, UCLÃ, Clara Lima, Major RD e Yunk Vino no palco Grillz. Já no World estavam Cone Crew, MV Bill e Kmila CDD, Flora Matos, BIN, Orochi, Racionais, Teto e Matuê, que fechou o festival. .</p>
